package com.ibm.automation.core.exception.handler;

public class ReturnToLastPageException  extends RuntimeException{

	/**
	 * 
	 */
	private static final long serialVersionUID = -2860292935782379987L;


	/**
	 * 
	 */
	

	public ReturnToLastPageException() {
	}

	public ReturnToLastPageException(String arg0) {
		super(arg0);
	}

	public ReturnToLastPageException(Throwable arg0) {
		super(arg0);
	}

	
	public ReturnToLastPageException(String arg0, Throwable arg1) {
		super(arg0, arg1);
	}
}