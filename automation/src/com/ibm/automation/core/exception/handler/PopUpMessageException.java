package com.ibm.automation.core.exception.handler;

public class PopUpMessageException extends RuntimeException{

	/**
	 * 
	 */
	private static final long serialVersionUID = -5008092781427556056L;


	/**
	 * 
	 */
	

	public PopUpMessageException() {
	}

	public PopUpMessageException(String arg0) {
		super(arg0);
	}

	public PopUpMessageException(Throwable arg0) {
		super(arg0);
	}

	
	public PopUpMessageException(String arg0, Throwable arg1) {
		super(arg0, arg1);
	}
}