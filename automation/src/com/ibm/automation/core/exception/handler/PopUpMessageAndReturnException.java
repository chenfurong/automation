package com.ibm.automation.core.exception.handler;

public class PopUpMessageAndReturnException extends RuntimeException{

	/**
	 * 
	 */
	private static final long serialVersionUID = -5008092781427556056L;


	/**
	 * 
	 */
	

	public PopUpMessageAndReturnException() {
	}

	public PopUpMessageAndReturnException(String arg0) {
		super(arg0);
	}

	public PopUpMessageAndReturnException(Throwable arg0) {
		super(arg0);
	}

	
	public PopUpMessageAndReturnException(String arg0, Throwable arg1) {
		super(arg0, arg1);
	}
}